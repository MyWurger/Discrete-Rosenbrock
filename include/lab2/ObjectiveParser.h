#pragma once

// Подключаем интерфейс целевой функции, который должен вернуть парсер.
#include "ObjectiveFunction.h"

// std::string нужен для текста выражения, имени функции и сообщения об ошибке.
#include <string>

namespace lab2
{

// ----------------------------------------------------------------------------
// ИНТЕРФЕЙС IObjectiveParser - АБСТРАКТНЫЙ ПАРСЕР СТРОКОВОЙ ФОРМУЛЫ
// ----------------------------------------------------------------------------
// Реализация этого интерфейса должна:
// - принять текст выражения;
// - проверить его корректность;
// - создать объект IObjectiveFunction, пригодный для дальнейших вычислений.
// ----------------------------------------------------------------------------
class IObjectiveParser
{
public:
    // Виртуальный деструктор нужен для корректного удаления через указатель на базу.
    virtual ~IObjectiveParser()
    {
    }

    // Разбирает текстовое выражение и создает объект функции.
    // Принимает:
    // - expression: текст математической формулы;
    // - dimension: ожидаемая размерность пространства;
    // - name: имя функции, которое будет сохранено внутри создаваемого объекта;
    // - output: ссылка на указатель, куда записывается созданный объект функции;
    // - error: строка, в которую записывается текст ошибки.
    // Возвращает:
    // - true, если парсинг завершился успешно;
    // - false, если выражение некорректно или объект создать не удалось.
    virtual bool Parse(const std::string& expression,
                       std::size_t dimension,
                       const std::string& name,
                       IObjectiveFunction*& output,
                       std::string& error) const = 0;
};

}  // namespace lab2
