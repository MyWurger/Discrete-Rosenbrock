// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем интерфейс вычислимой целевой функции.
#include "ObjectiveFunction.h"
// Подключаем тип результата оптимизации и структуры трассировки метода.
#include "RosenbrockDiscreteMethod.h"

// std::function нужен для callback-сигнала о смене шага в окне визуализации.
#include <functional>
// std::numeric_limits нужен для специального значения "начальный шаг не задан".
#include <limits>
// std::string нужен для возврата текста ошибки при неудачном открытии окна.
#include <string>

namespace lab2
{

// ----------------------------------------------------------------------------
// ТИП ViewerStepChangedCallback - CALLBACK ДЛЯ СИНХРОНИЗАЦИИ С ГЛАВНЫМ ОКНОМ
// ----------------------------------------------------------------------------
// Callback вызывается при смене отображаемого шага в 3D-окне визуализации.
// Аргументы:
// - shownStep: номер показываемого шага;
// - totalSteps: общее число шагов;
// - k: номер внешней итерации, соответствующей текущему шагу.
// ----------------------------------------------------------------------------
#ifndef LAB2_VIEWER_STEP_CALLBACK_DEFINED
#define LAB2_VIEWER_STEP_CALLBACK_DEFINED
typedef std::function<void(std::size_t, std::size_t, std::size_t)> ViewerStepChangedCallback;
#endif

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ShowRosenbrock3DViewer - ОТКРЫТИЕ ОКНА 3D-ВИЗУАЛИЗАЦИИ
// ----------------------------------------------------------------------------
// Открывает отдельное окно, в котором показываются:
// - траектория метода в трехмерном пространстве;
// - проекции на разные оси/срезы;
// - управление шагами, камерой и анимацией.
// Принимает:
// - function: целевая функция задачи;
// - result: результат оптимизации с полной трассировкой;
// - error: строка для текста ошибки при неудаче;
// - initialStep: шаг, который нужно показать при старте окна;
// - onStepChanged: callback для синхронизации с таблицей в основном окне.
// Возвращает:
// - true, если окно визуализации удалось открыть;
// - false, если открыть его не удалось.
// ----------------------------------------------------------------------------
bool ShowRosenbrock3DViewer(const IObjectiveFunction* function,
                            const OptimizationResult& result,
                            std::string& error,
                            std::size_t initialStep = std::numeric_limits<std::size_t>::max(),
                            const ViewerStepChangedCallback& onStepChanged = ViewerStepChangedCallback());

}  // namespace lab2
