// Подключаем интерфейс вычислимой целевой функции.
#include "ObjectiveFunction.h"
// Подключаем тип результата оптимизации и связанные структуры трассировки.
#include "RosenbrockDiscreteMethod.h"

// std::function нужен для callback-сигнала о смене шага в окне графика.
#include <functional>
// std::numeric_limits нужен для специального значения "шаг не задан".
#include <limits>
// std::string нужен для текста ошибки при неудачном открытии окна.
#include <string>

