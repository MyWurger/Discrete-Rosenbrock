{

// ----------------------------------------------------------------------------
// ТИП ViewerStepChangedCallback - CALLBACK ДЛЯ СИНХРОНИЗАЦИИ С ГЛАВНЫМ ОКНОМ
// ----------------------------------------------------------------------------
// Callback вызывается при смене отображаемого шага в окне визуализации.
// Аргументы:
// - shownStep: номер показываемого шага;
// - totalSteps: общее число шагов;
// - k: номер внешней итерации, которой соответствует текущий шаг.
// ----------------------------------------------------------------------------
#ifndef LAB2_VIEWER_STEP_CALLBACK_DEFINED
#define LAB2_VIEWER_STEP_CALLBACK_DEFINED
typedef std::function<void(std::size_t, std::size_t, std::size_t)> ViewerStepChangedCallback;
#endif

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ShowRosenbrock2DViewer - ОТКРЫТИЕ ОКНА 2D-ВИЗУАЛИЗАЦИИ
// ----------------------------------------------------------------------------
// Открывает отдельное окно, в котором показываются:
// - траектория метода Розенброка на плоскости;
// - линии уровня функции;
// - управление шагами и анимацией.
// Принимает:
// - function: целевая функция двух переменных;
// - result: результат оптимизации с полной трассировкой;
// - error: строка, в которую будет записан текст ошибки при неудаче;
// - initialStep: шаг, который нужно показать сразу после открытия;
// - onStepChanged: callback для синхронизации графика и таблицы.
// Возвращает:
// - true, если окно было успешно открыто;
// - false, если визуализацию запустить не удалось.
// ----------------------------------------------------------------------------
bool ShowRosenbrock2DViewer(const IObjectiveFunction* function,
                            const OptimizationResult& result,
                            std::string& error,
                            std::size_t initialStep = std::numeric_limits<std::size_t>::max(),
                            const ViewerStepChangedCallback& onStepChanged = ViewerStepChangedCallback());

}  // namespace lab2
