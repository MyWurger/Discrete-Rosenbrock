// Назначение файла:
// 1) Объявить конкретную реализацию интерфейса IObjectiveParser.
// 2) Связать текстовое выражение пользователя с библиотекой muparser.
// 3) Инкапсулировать создание объектов IObjectiveFunction из строковой формулы.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем общий интерфейс парсера целевой функции.
#include "ObjectiveParser.h"

namespace lab2
{

// ----------------------------------------------------------------------------
// КЛАСС MuParserObjectiveParser - КОНКРЕТНЫЙ ПАРСЕР ФУНКЦИЙ ЧЕРЕЗ MUPARSER
// ----------------------------------------------------------------------------
// Этот класс отвечает за:
// - разбор пользовательского выражения;
// - регистрацию переменных x1, x2, ..., xn;
// - создание объекта IObjectiveFunction, который затем можно передать методу.
// ----------------------------------------------------------------------------
class MuParserObjectiveParser : public IObjectiveParser
{
public:
    // Разбирает текстовое выражение и создает готовую целевую функцию.
    // Принимает:
    // - expression: текст математической формулы;
    // - dimension: ожидаемая размерность задачи;
    // - name: человеко-читаемое имя функции для отчетов и GUI;
    // - output: указатель, в который будет записан созданный объект функции;
    // - error: строка для текста ошибки, если парсинг завершится неудачно.
    // Возвращает:
    // - true, если функция успешно создана;
    // - false, если выражение некорректно или создать объект не удалось.
    bool Parse(const std::string& expression,
               std::size_t dimension,
               const std::string& name,
               IObjectiveFunction*& output,
               std::string& error) const;
};

}  // namespace lab2
