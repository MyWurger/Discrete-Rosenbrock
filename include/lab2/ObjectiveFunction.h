// Защита от повторного включения заголовочного файла.
#pragma once

// std::isfinite и другие численные утилиты могут использоваться в реализациях функций.
#include <cmath>
// std::size_t нужен для размерности и индексов координат.
#include <cstddef>
// std::string нужен для имени функции и текста ошибок вычисления.
#include <string>
// std::vector нужен для хранения координат точки x.
#include <vector>

namespace lab2
{

// ----------------------------------------------------------------------------
// ТИП Vector - ВЕКТОР КООРДИНАТ ТОЧКИ В ПРОСТРАНСТВЕ ПЕРЕМЕННЫХ
// ----------------------------------------------------------------------------
// Используется для:
// - хранения x = (x1, x2, ..., xn);
// - передачи точки в Evaluate(...);
// - хранения результатов метода и стационарной точки.
// ----------------------------------------------------------------------------
typedef std::vector<double> Vector;

// ----------------------------------------------------------------------------
// ИНТЕРФЕЙС IObjectiveFunction - АБСТРАКТНАЯ ЦЕЛЕВАЯ ФУНКЦИЯ
// ----------------------------------------------------------------------------
// Через этот интерфейс метод Розенброка работает с функцией независимо от того,
// как она была создана: вручную, через muparser или иным способом.
// ----------------------------------------------------------------------------
class IObjectiveFunction
{
public:
    // Виртуальный деструктор нужен для корректного удаления через указатель на базу.
    virtual ~IObjectiveFunction()
    {
    }

    // Возвращает размерность пространства, в котором задана функция.
    virtual std::size_t Dimension() const = 0;

    // Вычисляет значение функции в точке x.
    // Принимает:
    // - x: вектор координат точки;
    // - value: ссылка, куда записывается значение функции при успехе;
    // - error: строка для текста ошибки при неудачном вычислении.
    // Возвращает:
    // - true, если значение успешно вычислено;
    // - false, если вычисление выполнить не удалось.
    virtual bool Evaluate(const Vector& x, double& value, std::string& error) const = 0;

    // Возвращает имя функции для таблиц, GUI и отчетов.
    virtual const std::string& Name() const = 0;
};

}  // namespace lab2
