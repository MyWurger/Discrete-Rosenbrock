// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::EVENTFILTER
// ----------------------------------------------------------------------------
// Назначение:
// Перехватывать события прокрутки для таблицы трассировки и текстовых полей,
// чтобы задать более удобное поведение тачпада и колесика мыши.
//
// Принимает:
// - watched : объект, на который пришло событие;
// - event   : само событие Qt.
//
// Возвращает:
// - true, если событие обработано здесь и дальше передавать его не нужно;
// - false, если событие должно пойти в стандартную обработку QMainWindow.
// ----------------------------------------------------------------------------
bool MainWindow::eventFilter(QObject* watched, QEvent* event)
{
    // Нас интересуют только события прокрутки.
    // Отдельно обрабатываем только события колеса/тачпада, все прочее пропускаем в базовый класс.
    if (event != NULL && event->type() == QEvent::Wheel)
    {
        // Первая специальная ветка — прокрутка именно внутри viewport таблицы трассировки.
        if (traceTable_ != NULL && watched == traceTable_->viewport())
        {
            // static_cast безопасен, потому что выше уже проверен точный тип
            // события QEvent::Wheel.
            QWheelEvent* wheel = static_cast<QWheelEvent*>(event);

            // pixelDelta() используется у тачпадов и дает "плавную" дельту
            // в пикселях. Если она пуста, берем более грубую angleDelta().
            const int rawDx =
                (wheel->pixelDelta().x() != 0) ? wheel->pixelDelta().x() : wheel->angleDelta().x();
            const int rawDy =
                (wheel->pixelDelta().y() != 0) ? wheel->pixelDelta().y() : wheel->angleDelta().y();

            // Немного уменьшаем чувствительность тачпада внутри таблицы, чтобы
            // горизонтальная и вертикальная прокрутка не были слишком резкими.
            const double scale = 0.75;

            // После масштабирования получаем итоговые величины прокрутки.
            const int dx = static_cast<int>(rawDx * scale);
            const int dy = static_cast<int>(rawDy * scale);

            // Берем реальные полосы прокрутки таблицы.
            QScrollBar* hbar = traceTable_->horizontalScrollBar();
            QScrollBar* vbar = traceTable_->verticalScrollBar();
            // Горизонтальную полосу двигаем только если она существует и есть ненулевая дельта.
            if (hbar != NULL && dx != 0)
            {
                // Для горизонтального скролла вычитаем dx, потому что знак
                // wheel-дельты и направление движения полосы в Qt противоположны
                // ожидаемому визуальному эффекту.
                hbar->setValue(hbar->value() - dx);
            }
            // Вертикальную полосу прокручиваем по тем же правилам.
            if (vbar != NULL && dy != 0)
            {
                // То же правило применяем и к вертикальной полосе.
                vbar->setValue(vbar->value() - dy);
            }
            // Если действительно был сдвиг хотя бы по одной оси, событие считаем полностью обработанным.
            if (dx != 0 || dy != 0)
            {
                // accept() помечает событие как обработанное текущим окном.
                event->accept();

                // true говорит Qt не передавать это же событие дальше.
                return true;
            }
        }

        // qobject_cast — Qt-аналог безопасного приведения типа для QObject.
        // Он вернет либо корректный QLineEdit*, либо NULL.
        QLineEdit* edit = qobject_cast<QLineEdit*>(watched);
        // Вторая специальная ветка — колесо мыши/тачпада над полем ввода.
        if (edit != NULL)
        {
            // Здесь снова безопасно приводим тип события к QWheelEvent.
            QWheelEvent* wheel = static_cast<QWheelEvent*>(event);

            // Сначала пытаемся взять горизонтальную дельту прокрутки.
            int delta = wheel->pixelDelta().x();
            // Если горизонтальная pixelDelta отсутствует, пробуем angleDelta по той же оси.
            if (delta == 0)
            {
                // Если пиксельная дельта отсутствует, используем угловую.
                delta = wheel->angleDelta().x();
            }
            // Если горизонтальная ось недоступна, переходим к вертикальной как fallback.
            if (delta == 0)
            {
                // Некоторые тачпады отдают только вертикальную компоненту даже
                // при жесте горизонтальной прокрутки, поэтому используем
                // запасную ветку.
                delta = wheel->pixelDelta().y();
                // Последняя попытка получить ненулевую дельту — вертикальная angleDelta().
                if (delta == 0)
                {
                    // И в самом последнем fallback берем вертикальную angleDelta().
                    delta = wheel->angleDelta().y();
                }
            }

            // Обрабатывать позицию курсора имеет смысл только при реально ненулевой дельте прокрутки.
            if (delta != 0)
            {
                // По знаку дельты определяем, двигаем ли курсор влево или вправо.
                const int direction = (delta > 0) ? -1 : 1;

                // Текущая позиция текстового курсора внутри строки.
                const int currentPos = edit->cursorPosition();

                // Пытаемся сместить курсор на один символ в выбранную сторону.
                int nextPos = currentPos + direction;
                // Слева ограничиваем позицию нулем, чтобы не выйти за начало строки.
                if (nextPos < 0)
                {
                    // Левее начала строки выходить нельзя.
                    nextPos = 0;
                }

                // Максимально допустимая позиция курсора равна длине текста.
                const int maxPos = edit->text().size();
                // Справа ограничиваем позицию длиной текста, чтобы не выйти за конец строки.
                if (nextPos > maxPos)
                {
                    // Правее конца строки тоже выходить нельзя.
                    nextPos = maxPos;
                }

                // Вместо изменения значения поля колесом двигаем курсор по
                // строке влево/вправо — это аккуратнее для длинных выражений.
                edit->setCursorPosition(nextPos);
                event->accept();
                return true;
            }
        }
    }

    // Все остальные события передаем стандартной реализации QMainWindow.
    return QMainWindow::eventFilter(watched, event);
}

