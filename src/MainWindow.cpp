// ----------------------------------------------------------------------------
// Назначение:
// Обновить блок аналитической стационарной точки, если он доступен.
// ----------------------------------------------------------------------------
void MainWindow::UpdateStationaryPanel()
{
    // Если аналитический расчет недоступен, панель целиком заполняем прочерками.
    if (!stationaryAvailable_)
    {
        // При отсутствии аналитического результата координаты точки скрываем прочерком.
        stationaryPointValueLabel_->setText(QString::fromUtf8("-"));

        // Значение функции в стационарной точке тоже в этот момент неизвестно.
        stationaryFunctionValueLabel_->setText(QString::fromUtf8("-"));

        // Расстояние до численного решения без аналитической точки определить нельзя.
        stationaryDistanceValueLabel_->setText(QString::fromUtf8("-"));

        // Разность значений функции между решениями также недоступна.
        stationaryDeltaValueLabel_->setText(QString::fromUtf8("-"));
        return;
    }

    // При наличии аналитического решения выводим его координаты.
    stationaryPointValueLabel_->setText(FormatVector(stationaryPoint_, 6));

    // Здесь показываем значение функции в аналитической стационарной точке.
    stationaryFunctionValueLabel_->setText(FormatDouble(stationaryValue_, 12));

    // Это поле показывает евклидово расстояние до точки, найденной методом.
    stationaryDistanceValueLabel_->setText(FormatDouble(stationaryDistance_, 12));

    // Здесь выводится разность значений функции между аналитическим и численным решениями.
    stationaryDeltaValueLabel_->setText(FormatDouble(stationaryDelta_, 12));
}

// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::HIGHLIGHTTABLEROWBYSTEP
// ----------------------------------------------------------------------------
// Назначение:
// Подсветить строку таблицы, соответствующую текущему шагу анимации графика.
// ----------------------------------------------------------------------------
void MainWindow::HighlightTableRowByStep(std::size_t shownStep, std::size_t totalSteps, std::size_t)
{
    // Без результата или без таблицы подсветка строк невозможна.
    if (!hasResult_ || traceTable_ == NULL)
    {
        // Без результата или без самой таблицы подсвечивать нечего.
        return;
    }

    // summaryRow — индекс строки ИТОГ после последней реальной строки трассы.
    const int summaryRow = static_cast<int>(currentResult_.trace.size()) + 1;

    // totalRows — фактическое число строк таблицы, включая служебные строки.
    const int totalRows = traceTable_->rowCount();
    // Если таблица пуста, выбирать в ней просто нечего.
    if (totalRows <= 0)
    {
        // Пустая таблица не содержит строк для подсветки.
        return;
    }

    // Перед новой подсветкой снимаем предыдущее выделение строк.
    traceTable_->clearSelection();

    // row будет хранить индекс строки, которую нужно выделить для текущего шага.
    int row = -1;
    // Шаг 0 в графике соответствует специальной строке НАЧАЛО.
    if (shownStep == 0)
    {
        // Шаг 0 соответствует служебной строке НАЧАЛО.
        row = 0;
    }
    // Промежуточные шаги анимации отображаются строками трассы с индексом shownStep.
    else if (shownStep < totalSteps)
    {
        // Промежуточные шаги напрямую сопоставляются строкам трассы.
        row = static_cast<int>(shownStep);
    }
    // Последний или выходящий за диапазон шаг интерпретируем как переход к строке ИТОГ.
    else if (shownStep >= totalSteps)
    {
        // После последнего шага выделяем строку ИТОГ.
        row = summaryRow;
    }

    // Строку выделяем только если рассчитанный индекс реально попадает в таблицу.
    if (row >= 0 && row < totalRows)
    {
        // Выделяем найденную строку целиком.
        traceTable_->selectRow(row);

        // firstCell — первая ячейка строки, по которой удобно выполнить прокрутку.
        QTableWidgetItem* firstCell = traceTable_->item(row, 0);
        // Прокрутка к строке возможна только если в ней существует хотя бы первая ячейка.
        if (firstCell != NULL)
        {
            // Прокручиваем таблицу так, чтобы активная строка была примерно по центру.
            traceTable_->scrollToItem(firstCell, QAbstractItemView::PositionAtCenter);
        }
    }
}

// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::SOLVE
// ----------------------------------------------------------------------------
// Назначение:
// Считать входные параметры из GUI, запустить оптимизацию и обновить все
// панели результата и таблицу.
// ----------------------------------------------------------------------------
void MainWindow::Solve()
{
    // Перед новым расчетом удаляем ранее созданную пользовательскую функцию.
    DeleteCurrentFunction();

    // И сразу сбрасываем GUI к чистому состоянию без старых результатов.
    ClearResultState();

    // request будет содержать полный набор параметров для оптимизатора.
    OptimizationRequest request;

    // expression хранит текст выражения в формате std::string для парсера и метода.
    std::string expression;

    // dimension получит распознанную размерность пользовательской функции.
    std::size_t dimension = 0;

    // buildError хранит понятный текст ошибки при чтении параметров из GUI.
    QString buildError;

    // Если из GUI не удалось собрать корректный запрос, оптимизацию даже не запускаем.
    if (!BuildRequestFromInput(request, expression, dimension, buildError))
    {
        // Если собрать корректный запрос не удалось, показываем причину в строке статуса.
        SetStatus(buildError, false);
        return;
    }

    // currentFunction_ хранит текущую функцию в поле класса, чтобы потом ее могли
    // использовать графики, экспорт и аналитические helper-функции.
    currentFunction_ = const_cast<IObjectiveFunction*>(request.function);

    // currentRequest_ сохраняет успешно собранный запрос целиком для последующих действий GUI.
    currentRequest_ = request;

    // optimizeError получит текст ошибки, если оптимизатор не сможет завершить расчет.
    std::string optimizeError;
    // Если сам алгоритм завершился вычислительной ошибкой, показываем ее и выходим.
    if (!optimizer_.Optimize(currentRequest_, currentResult_, optimizeError))
    {
        // При вычислительной ошибке показываем подробное сообщение пользователю.
        SetStatus(QString::fromUtf8("Ошибка оптимизации: %1").arg(QString::fromStdString(optimizeError)), false);

        // Если расчет сорвался, временно созданную функцию нужно освободить.
        DeleteCurrentFunction();
        return;
    }

    // После успешной оптимизации помечаем, что результат уже доступен.
    hasResult_ = true;

    // Теперь кнопку открытия графика можно разблокировать.
    openGraphButton_->setEnabled(true);

    // И кнопку экспорта таблицы тоже.
    exportTraceButton_->setEnabled(true);

    // stationaryReport временно собирает аналитическую информацию через производную.
    StationaryPointReport stationaryReport;

    // Пытаемся построить стационарную точку аналитически для сравнения с методом.
    BuildStationaryPointByDerivative(currentFunction_, currentResult_, stationaryReport);

    // Флаг показывает, удалось ли построить аналитический отчет.
    stationaryAvailable_ = stationaryReport.available;

    // Сохраняем найденную аналитическую точку в поле класса.
    stationaryPoint_ = stationaryReport.point;

    // Сохраняем значение функции в аналитической точке.
    stationaryValue_ = stationaryReport.value;

    // Сохраняем расстояние между аналитической и численной точками.
    stationaryDistance_ = stationaryReport.distanceToMethodPoint;

    // Сохраняем разность значений функции между двумя решениями.
    stationaryDelta_ = stationaryReport.deltaValueToMethod;

    // Сохраняем диагностическое сообщение аналитического блока.
    stationaryMessage_ = stationaryReport.message;

    // Заполняем таблицу трассировки строками полученного результата.
    FillTraceTable();

    // Обновляем краткую итоговую панель численного решения.
    UpdateSummaryPanel();

    // Обновляем аналитическую панель стационарной точки.
    UpdateStationaryPanel();

    // В строке статуса показываем, достигнута ли сходимость по мнению алгоритма.
    SetStatus(QString::fromUtf8("Расчет завершен: сходимость %1")
                  .arg(currentResult_.converged ? QString::fromUtf8("достигнута") : QString::fromUtf8("не достигнута")),
              currentResult_.converged);

    // После расчета сразу подсвечиваем в таблице итоговую строку.
    HighlightTableRowByStep(currentResult_.trace.size(), currentResult_.trace.size(), currentResult_.iterations);
}

// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::OPENGRAPHWINDOW
// ----------------------------------------------------------------------------
// Назначение:
// Открыть отдельное окно визуализации 2D или 3D для уже рассчитанного
// результата метода.
// ----------------------------------------------------------------------------
void MainWindow::OpenGraphWindow()
{
    // Окно графика можно открывать только после успешного расчета и при наличии текущей функции.
    if (!hasResult_ || currentFunction_ == NULL)
    {
        // Без выполненного расчета и без текущей функции окно визуализации открыть нельзя.
        ShowLightMessageBox(this,
                            QMessageBox::Warning,
                            QString::fromUtf8("Нет данных"),
                            QString::fromUtf8("Сначала выполните расчет."));
        return;
    }

    // initialStep задает, с какого шага график откроется изначально.
    std::size_t initialStep = std::numeric_limits<std::size_t>::max();

    // selectedRow — текущая выделенная строка таблицы трассировки.
    const int selectedRow = traceTable_->currentRow();

    // traceRows — число реальных строк трассы без служебных строк НАЧАЛО/ИТОГ.
    const int traceRows = static_cast<int>(currentResult_.trace.size());

    // summaryRow — индекс строки ИТОГ в таблице.
    const int summaryRow = traceRows + 1;
    // Строка НАЧАЛО соответствует нулевому шагу анимации.
    if (selectedRow == 0)
    {
        // Если выделена строка НАЧАЛО, открываем график с шага 0.
        initialStep = 0;
    }
    // Обычные строки трассы напрямую переводятся в соответствующий номер шага.
    else if (selectedRow >= 1 && selectedRow <= traceRows)
    {
        // Если выделена обычная строка трассы, открываем график с соответствующего шага.
        initialStep = static_cast<std::size_t>(selectedRow);
    }
    // Строка ИТОГ должна открывать viewer уже на финальном шаге.
    else if (selectedRow == summaryRow)
    {
        // Если выделена строка ИТОГ, открываем график на последнем шаге.
        initialStep = currentResult_.trace.size();
    }

    // Лямбда-прослойка связывает окно графика и таблицу трассировки:
    // при перемещении по шагам в графике обновляется подсветка строки в GUI.
    ViewerStepChangedCallback onStepChanged =
        [this](std::size_t shownStep, std::size_t totalSteps, std::size_t k)
        {
            HighlightTableRowByStep(shownStep, totalSteps, k);
        };

    // error получит текст ошибки, если окно визуализации не откроется.
    std::string error;

    // ok отражает итоговый статус попытки открыть viewer.
    bool ok = false;
    // Для двумерной функции открываем специализированный 2D viewer.
    if (currentFunction_->Dimension() == 2)
    {
        // Для двумерной функции запускаем 2D viewer.
        ok = ShowRosenbrock2DViewer(currentFunction_, currentResult_, error, initialStep, onStepChanged);
    }
    // Для трехмерной функции, соответственно, запускаем 3D viewer.
    else if (currentFunction_->Dimension() == 3)
    {
        // Для трехмерной функции запускаем 3D viewer.
        ok = ShowRosenbrock3DViewer(currentFunction_, currentResult_, error, initialStep, onStepChanged);
    } else {
        // Все остальные размерности для текущей визуализации не поддерживаются.
        error = "Поддерживается только 2D/3D визуализация.";
    }

    // Если открыть viewer не удалось, обязательно сообщаем об этом пользователю.
    if (!ok)
    {
        // Если viewer не открылся, сначала показываем краткий статус в главном окне.
        SetStatus(QString::fromUtf8("Ошибка визуализации"), false);

        // А затем выводим пользователю подробное диалоговое сообщение.
        ShowLightMessageBox(this,
                            QMessageBox::Warning,
                            QString::fromUtf8("Ошибка визуализации"),
                            QString::fromStdString(error));
    }
}

// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::EXPORTTRACETABLEPNG
// ----------------------------------------------------------------------------
// Назначение:
// Выгрузить текущую таблицу трассировки целиком в PNG-файл внутри папки
// images проекта.
// ----------------------------------------------------------------------------
void MainWindow::ExportTraceTablePng()
{
    // Экспорт возможен только когда таблица уже построена и содержит хотя бы одну строку.
    if (!hasResult_ || traceTable_ == NULL || traceTable_->rowCount() == 0)
    {
        // Экспорт невозможен, если таблица еще не построена или расчет не выполнялся.
        ShowLightMessageBox(this,
                            QMessageBox::Warning,
                            QString::fromUtf8("Нет данных"),
                            QString::fromUtf8("Сначала выполните расчет, затем сохраните таблицу."));
        return;
    }

    // exportCard — временный невидимый контейнер, в который собираем копию
    // заголовка и таблицы специально для offscreen-рендера в PNG.
    QWidget exportCard;

    // objectName нужен, чтобы применить к экспортируемой карточке тот же стиль, что и в GUI.
    exportCard.setObjectName("outputCard");

    // Переносим на временную карточку текущий stylesheet окна.
    exportCard.setStyleSheet(styleSheet());

    // Этот атрибут запрещает показ виджета на экране: он нужен только для рендера в картинку.
    exportCard.setAttribute(Qt::WA_DontShowOnScreen, true);

    // exportLayout — вертикальная раскладка внутри временной карточки.
    QVBoxLayout* exportLayout = new QVBoxLayout(&exportCard);

    // Повторяем те же отступы, что и у правой панели основного GUI.
    exportLayout->setContentsMargins(14, 14, 14, 14);

    // И тот же интервал между заголовком и таблицей.
    exportLayout->setSpacing(8);

    // exportTitle — копия заголовка таблицы для экспортируемой карточки.
    QLabel* exportTitle = new QLabel(TraceTableTitleText(), &exportCard);

    // objectName нужен, чтобы этот заголовок был стилизован так же, как в основном окне.
    exportTitle->setObjectName("traceTitle");

    // Добавляем заголовок в верхнюю часть экспортируемой карточки.
    exportLayout->addWidget(exportTitle);

    // exportTable — отдельная оффскрин-копия таблицы, которую будем рендерить в PNG.
    QTableWidget* exportTable = new QTableWidget(&exportCard);

    // Поведение выделения строк копируем из исходной таблицы.
    exportTable->setSelectionBehavior(traceTable_->selectionBehavior());

    // Режим выбора строк тоже копируем из исходной таблицы.
    exportTable->setSelectionMode(traceTable_->selectionMode());

    // Редактирование в экспортной копии тоже не требуется.
    exportTable->setEditTriggers(QAbstractItemView::NoEditTriggers);

    // Чередование цветов строк переносим как есть.
    exportTable->setAlternatingRowColors(traceTable_->alternatingRowColors());

    // Рисование сетки тоже копируем из исходной таблицы.
    exportTable->setShowGrid(traceTable_->showGrid());

    // Перенос строк внутри ячеек переносим в экспортную таблицу.
    exportTable->setWordWrap(traceTable_->wordWrap());

    // Режим обрезки текста многоточием тоже копируем.
    exportTable->setTextElideMode(traceTable_->textElideMode());

    // Полосы прокрутки в PNG не нужны, поэтому принудительно скрываем вертикальную.
    exportTable->setVerticalScrollBarPolicy(Qt::ScrollBarAlwaysOff);

    // И горизонтальную полосу прокрутки тоже скрываем.
    exportTable->setHorizontalScrollBarPolicy(Qt::ScrollBarAlwaysOff);

    // Состояние вертикального заголовка копируем у оригинальной таблицы.
    exportTable->verticalHeader()->setVisible(traceTable_->verticalHeader()->isVisible());

    // Флаг растяжения последней секции тоже переносим.
    exportTable->horizontalHeader()->setStretchLastSection(traceTable_->horizontalHeader()->stretchLastSection());

    // Минимальную ширину секций также копируем для совпадения внешнего вида.
    exportTable->horizontalHeader()->setMinimumSectionSize(traceTable_->horizontalHeader()->minimumSectionSize());

    // Число столбцов экспортной таблицы должно совпадать с оригиналом.
    exportTable->setColumnCount(traceTable_->columnCount());

    // И число строк тоже должно быть точно таким же.
    exportTable->setRowCount(traceTable_->rowCount());

    // headers — локальный список заголовков столбцов экспортной таблицы.
    QStringList headers;

    // column — индекс текущего столбца при копировании заголовков.
    int column = 0;
    for (column = 0; column < traceTable_->columnCount(); ++column)
    {
        // headerItem — указатель на исходный заголовок текущего столбца.
        QTableWidgetItem* headerItem = traceTable_->horizontalHeaderItem(column);

        // Если заголовок существует, берем его текст, иначе добавляем пустую строку.
        headers << (headerItem != NULL ? headerItem->text() : QString::fromUtf8(""));
    }

    // Устанавливаем экспортной таблице собранный набор заголовков.
    exportTable->setHorizontalHeaderLabels(headers);

    // row — индекс текущей строки при копировании содержимого таблицы.
    int row = 0;
    for (row = 0; row < traceTable_->rowCount(); ++row)
    {
        // Высоту каждой строки копируем у исходной таблицы, чтобы экспорт совпадал визуально.
        exportTable->setRowHeight(row, traceTable_->rowHeight(row));

        for (column = 0; column < traceTable_->columnCount(); ++column)
        {
            // sourceItem — исходная ячейка, существующая в основной таблице.
            QTableWidgetItem* sourceItem = traceTable_->item(row, column);

            // targetItem — новая ячейка, которая будет помещена в экспортную копию.
            QTableWidgetItem* targetItem = NULL;
            // Если исходная ячейка существует, ее безопаснее клонировать вместе со всем оформлением.
            if (sourceItem != NULL)
            {
                // clone() копирует текст, стиль, фон, шрифт и прочие свойства исходной ячейки.
                targetItem = sourceItem->clone();
            } else {
                // Если исходная ячейка отсутствует, создаем пустую заглушку.
                targetItem = new QTableWidgetItem(QString::fromUtf8(""));

                // Пустую заглушку сразу центрируем, чтобы ее вид не выбивался из общей таблицы.
                targetItem->setTextAlignment(Qt::AlignCenter);
            }

            // Помещаем подготовленную ячейку в экспортную таблицу.
            exportTable->setItem(row, column, targetItem);
        }
    }

    for (column = 0; column < traceTable_->columnCount(); ++column)
    {
        // Восстанавливаем текущую ширину каждого столбца один в один с экранной таблицей.
        exportTable->setColumnWidth(column, traceTable_->columnWidth(column));

        // И копируем режим resize для каждой секции заголовка.
        exportTable->horizontalHeader()->setSectionResizeMode(
            column, traceTable_->horizontalHeader()->sectionResizeMode(column));
    }

    // selectedRow — строка, которая была выделена пользователем в основной таблице.
    const int selectedRow = traceTable_->currentRow();
    // Переносим выделение только если индекс выделенной строки остается корректным и в экспортной копии.
    if (selectedRow >= 0 && selectedRow < exportTable->rowCount())
    {
        // То же выделение переносим и в экспортную таблицу, чтобы PNG отражал текущее состояние GUI.
        exportTable->selectRow(selectedRow);
    }

    // exportWidth — итоговая ширина экспортной таблицы, не меньше 640 px.
    const int exportWidth = std::max(FullTableWidth(exportTable), 640);

    // exportHeight — итоговая высота экспортной таблицы, не меньше 240 px.
    const int exportHeight = std::max(FullTableHeight(exportTable), 240);

    // Жестко фиксируем минимальный размер экспортной таблицы перед рендером.
    exportTable->setMinimumSize(exportWidth, exportHeight);

    // И такой же максимальный размер, чтобы layout не пытался ее перерастягивать.
    exportTable->setMaximumSize(exportWidth, exportHeight);

    // После настройки размеров добавляем таблицу в экспортную карточку.
    exportLayout->addWidget(exportTable);

    // ensurePolished() принудительно завершает применение стилей к временной карточке.
    exportCard.ensurePolished();

    // activate() пересчитывает геометрию layout с учетом всех уже добавленных виджетов.
    exportLayout->activate();

    // layoutMargins — суммарный запас на внешние поля карточки по ширине/высоте.
    const int layoutMargins = 28;

    // layoutSpacing — вертикальный интервал между заголовком и таблицей.
    const int layoutSpacing = 8;

    // extraRightPadding — дополнительный запас справа, чтобы ничего не срезалось по рамке.
    const int extraRightPadding = 16;

    // extraBottomPadding — дополнительный запас снизу против обрезания последней линии.
    const int extraBottomPadding = 28;

    // titleHeight — высота заголовка экспорта, но не меньше 28 px для устойчивого вида.
    const int titleHeight = std::max(exportTitle->sizeHint().height(), 28);

    // finalWidth — полная ширина карточки с учетом таблицы, заголовка и запасов по краям.
    const int finalWidth =
        std::max(exportWidth + layoutMargins + extraRightPadding, exportTitle->sizeHint().width() + layoutMargins);

    // finalHeight — полная высота карточки с учетом заголовка, таблицы и нижнего запаса.
    const int finalHeight = titleHeight + exportHeight + layoutMargins + layoutSpacing + extraBottomPadding;

    // Фиксируем реальный размер временной карточки перед ее рендером в изображение.
    exportCard.resize(finalWidth, finalHeight);

    // Явно задаем layout ту геометрию, которую только что вычислили.
    exportLayout->setGeometry(QRect(0, 0, finalWidth, finalHeight));

    // Повторно активируем раскладку уже с окончательной геометрией.
    exportLayout->activate();

    // imageSize — финальный размер PNG в пикселях.
    const QSize imageSize(finalWidth, finalHeight);

    // Создаем QImage с альфа-каналом и форматом, удобным для рендера виджетов Qt.
    QImage image(imageSize, QImage::Format_ARGB32_Premultiplied);

    // Перед рендером заливаем фон белым цветом.
    image.fill(Qt::white);

    // painter — объект рисования, в который exportCard будет себя рендерить.
    QPainter painter(&image);

    // render(...) переносит содержимое временной карточки прямо в изображение.
    exportCard.render(&painter);

    // Завершаем рисование явно, чтобы все данные гарантированно были записаны в image.
    painter.end();

    // imagesPath — абсолютный путь к папке images внутри проекта.
    const QString imagesPath = ProjectImagesDirectoryPath();

    // Базовое имя файла строим из текущих параметров расчета.
    QString fileName = BuildTraceExportFileName(expressionEdit_->text(), currentRequest_);

    // timeSuffix добавляет к имени штамп времени, чтобы новые файлы не затирали старые.
    const QString timeSuffix = QDateTime::currentDateTime().toString("yyyyMMdd_HHmmss");

    // Встраиваем штамп времени прямо перед расширением .png.
    fileName.replace(QString::fromUtf8(".png"), QString::fromUtf8("__%1.png").arg(timeSuffix));

    // filePath — полный путь, по которому PNG будет сохранен на диск.
    const QString filePath = QDir(imagesPath).absoluteFilePath(fileName);

    // Если QImage не смог сохраниться на диск, показываем ошибку и не сообщаем об успехе.
    if (!image.save(filePath, "PNG"))
    {
        // Если запись файла не удалась, сообщаем пользователю о проблеме.
        ShowLightMessageBox(this,
                            QMessageBox::Warning,
                            QString::fromUtf8("Ошибка сохранения"),
                            QString::fromUtf8("Не удалось сохранить PNG в папку images."));
        return;
    }

    // При успешном сохранении показываем точный путь к созданному PNG-файлу.
    ShowLightMessageBox(this,
                        QMessageBox::Information,
                        QString::fromUtf8("PNG сохранен"),
                        QString::fromUtf8("Таблица сохранена:\n%1").arg(filePath));
}

