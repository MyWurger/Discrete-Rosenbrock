// ----------------------------------------------------------------------------
// МЕТОД MAINWINDOW::SETUPSIGNALS
// ----------------------------------------------------------------------------
// Назначение:
// Соединить сигналы Qt-виджетов с обработчиками класса MainWindow.
// ----------------------------------------------------------------------------
void MainWindow::SetupSignals() 
{
    // Нажатие основной кнопки запускает расчет.
    connect(solveButton_, &QPushButton::clicked, this, &MainWindow::Solve);

    // Открытие графического окна вынесено в отдельный слот.
    connect(openGraphButton_, &QPushButton::clicked, this, &MainWindow::OpenGraphWindow);

    // Экспорт трассировки в PNG.
    connect(exportTraceButton_, &QPushButton::clicked, this, &MainWindow::ExportTraceTablePng);

    // Изменение выражения функции требует отдельной обработки, потому что
    // может измениться размерность и валидность выражения.
    connect(expressionEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleFunctionTextChanged);

    // Остальные поля используют общий обработчик изменения входных параметров.
    // Стартовая точка влияет на запрос метода и на проверку размерности вектора.
    connect(startPointEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleInputsChanged);

    // Изменение epsilon влияет на критерий остановки.
    connect(epsilonEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleInputsChanged);

    // Изменение alpha меняет правило растяжения шага.
    connect(alphaEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleInputsChanged);

    // Изменение beta меняет правило сжатия и смены знака шага.
    connect(betaEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleInputsChanged);

    // Изменение Delta0 влияет на начальные длины шагов метода.
    connect(stepEdit_, &QLineEdit::textChanged, this, &MainWindow::HandleInputsChanged);
}

