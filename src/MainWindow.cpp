// ----------------------------------------------------------------------------
// ФУНКЦИЯ FULLTABLEWIDTH
// ----------------------------------------------------------------------------
// Назначение:
// Посчитать полную фактическую ширину таблицы со всеми столбцами и рамками.
// ----------------------------------------------------------------------------
int FullTableWidth(const QTableWidget* table)
{
    // Без валидного указателя на таблицу вычислять ее ширину невозможно.
    if (table == NULL)
    {
        // Если таблица отсутствует, считать ширину нечего.
        return 0;
    }

    // frameWidth() возвращает толщину рамки с одной стороны, поэтому умножаем
    // на два для левой и правой границы.
    int width = table->frameWidth() * 2;
    // Если вертикальный заголовок отображается, его ширина тоже входит в общий размер таблицы.
    if (table->verticalHeader()->isVisible())
    {
        // Если вертикальный заголовок видим, его ширина тоже входит в общую
        // фактическую ширину таблицы.
        width += table->verticalHeader()->width();
    }

    // Индекс столбца объявляем отдельно в простом учебном стиле.
    int column = 0;
    for (column = 0; column < table->columnCount(); ++column)
    {
        // Последовательно добавляем фактическую ширину каждого столбца.
        width += table->columnWidth(column);
    }

    // Возвращаем суммарную ширину всей таблицы.
    return width;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FULLTABLEHEIGHT
// ----------------------------------------------------------------------------
// Назначение:
// Посчитать полную фактическую высоту таблицы со всеми строками и заголовком.
// ----------------------------------------------------------------------------
int FullTableHeight(const QTableWidget* table)
{
    // Без валидного указателя на таблицу вычислять ее высоту невозможно.
    if (table == NULL)
    {
        // Если таблица отсутствует, высота считается равной нулю.
        return 0;
    }

    // Начинаем с толщины внешней рамки сверху и снизу.
    int height = table->frameWidth() * 2;
    // Если горизонтальный заголовок видим, его высоту нужно учесть в полном размере.
    if (table->horizontalHeader()->isVisible())
    {
        // Если заголовок столбцов видим, его высота входит в полный размер.
        height += table->horizontalHeader()->height();
    }

    // Индекс строки объявляем заранее отдельной строкой.
    int row = 0;
    for (row = 0; row < table->rowCount(); ++row)
    {
        // Добавляем фактическую высоту каждой строки таблицы.
        height += table->rowHeight(row);
    }

    // Добавляем дополнительный запас снизу, чтобы при экспорте в PNG не
    // срезались последняя линия сетки и нижний отступ темы.
    height += 20;

    // Возвращаем итоговую расчетную высоту таблицы.
    return height;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ EVALUATEFUNCTIONVALUE
// ----------------------------------------------------------------------------
// Назначение:
// Безопасно вычислить значение целевой функции в точке x.
// ----------------------------------------------------------------------------
bool EvaluateFunctionValue(const IObjectiveFunction* function,
                           const Vector& x,
                           double& value,
                           std::string& error)
                           {
    // На всякий случай заранее сбрасываем значение, чтобы при ошибке не
    // осталось случайного старого числа.
    value = 0.0;

    // Если функция не передана, вычисление значения по определению невозможно.
    if (function == NULL)
    {
        // Без объекта функции вычислять значение просто нечем.
        error = "Функция не задана.";
        return false;
    }

    // Локальная строка собирает возможный текст ошибки от конкретной
    // реализации функции, не затирая основной error заранее.
    std::string evalError;

    // Само вычисление делегируется объекту функции.
    // Если конкретная реализация функции не смогла вычислить значение, пробрасываем ошибку наверх.
    if (!function->Evaluate(x, value, evalError))
    {
        // Тернарный оператор выбирает либо текст ошибки самой функции,
        // либо резервное сообщение, если та вернула пустую строку.
        error = evalError.empty() ? "Ошибка вычисления функции." : evalError;
        return false;
    }

    // Даже если Evaluate(...) формально вернул true, результат еще должен быть
    // конечным числом.
    // Даже успешный вызов Evaluate(...) дополнительно проверяем на NaN и бесконечность.
    if (!std::isfinite(value))
    {
        error = "Функция вернула нечисловое значение.";
        return false;
    }

    // true означает, что в value лежит корректное конечное значение f(x).
    return true;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ EVALUATEQUADRATICMODEL
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить значение восстановленной квадратичной модели в произвольной точке x.
//
// Принимает:
// - c0     : свободный коэффициент модели;
// - linear : коэффициенты линейных членов;
// - square : коэффициенты квадратов координат;
// - cross  : матрица коэффициентов смешанных членов x_i * x_j;
// - x      : точка, в которой надо вычислить модель.
//
// Возвращает:
// - значение квадратичной модели в точке x.
//
// Зачем нужна:
// Эта helper-функция используется при проверке, действительно ли введенная
// пользователем функция совпадает с квадратичной моделью, которую мы
// восстановили по нескольким контрольным точкам.
// ----------------------------------------------------------------------------
double EvaluateQuadraticModel(double c0,
                              const Vector& linear,
                              const Vector& square,
                              const std::vector<std::vector<double> >& cross,
                              const Vector& x)
                              {
    // Размерность модели берем из входного вектора точки, где она вычисляется.
    const std::size_t n = x.size();

    // Начинаем с свободного коэффициента квадратичной модели.
    double value = c0;

    // Индекс внешнего цикла объявляем заранее в учебном стиле.
    std::size_t i = 0;
    for (i = 0; i < n; ++i)
    {
        // Добавляем линейные и квадратные члены по каждой координате.
        value += linear[i] * x[i];
        value += square[i] * x[i] * x[i];
    }

    for (i = 0; i < n; ++i)
    {
        // Для смешанных членов x_i*x_j начинаем только со следующего индекса,
        // чтобы не дублировать симметричные пары дважды.
        std::size_t j = i + 1;
        for (; j < n; ++j)
        {
            // Добавляем смешанные члены x_i * x_j.
            value += cross[i][j] * x[i] * x[j];
        }
    }

    // Возвращаем значение квадратичной модели в точке x.
    return value;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ SOLVELINEARSYSTEM
// ----------------------------------------------------------------------------
// Назначение:
// Решить линейную систему Ax = b методом Гаусса с выбором главного элемента.
// ----------------------------------------------------------------------------
bool SolveLinearSystem(std::vector<std::vector<double> >& a, Vector& b, Vector& x)
{
    // Размер системы равен числу строк матрицы A.
    const std::size_t n = a.size();
    // Пустая система или несовпадение размеров матрицы и правой части делают задачу некорректной.
    if (n == 0 || b.size() != n)
    {
        // Пустая система или несовпадение размеров A и b делает задачу
        // некорректной.
        return false;
    }

    // Очень маленькие числа считаем численно нулевыми.
    const double eps = 1e-12;

    // Перебираем столбцы матрицы слева направо.
    std::size_t col = 0;
    for (col = 0; col < n; ++col)
    {
        // pivot хранит номер строки, которая станет ведущей на текущем шаге.
        // Изначально считаем ведущей текущую строку col.
        std::size_t pivot = col;

        // row используется для поиска лучшего ведущего элемента ниже и на
        // текущей строке.
        std::size_t row = col + 1;
        for (; row < n; ++row)
        {
            // Ищем строку с максимальным по модулю элементом в текущем столбце.
            // В качестве ведущей выбираем строку с максимальным по модулю элементом в текущем столбце.
            if (std::fabs(a[row][col]) > std::fabs(a[pivot][col]))
            {
                // Если нашли строку с более крупным по модулю элементом,
                // делаем именно ее новым кандидатом в ведущую.
                pivot = row;
            }
        }

        // Если даже лучший кандидат почти нулевой, матрица вырождена или плохо обусловлена.
        if (std::fabs(a[pivot][col]) < eps)
        {
            // Если даже лучший ведущий элемент практически нулевой, матрица
            // вырождена или почти вырождена.
            return false;
        }

        // Перестановка нужна только тогда, когда лучший ведущий элемент найден не в текущей строке.
        if (pivot != col)
        {
            // Меняем строки местами, чтобы главный элемент оказался на
            // диагонали.
            std::swap(a[pivot], a[col]);

            // Ту же перестановку нужно выполнить и в правой части b, иначе
            // система A x = b перестанет быть согласованной.
            std::swap(b[pivot], b[col]);
        }

        // Диагональный элемент текущей ведущей строки.
        const double diag = a[col][col];

        // j пробегает столбцы ведущей строки при ее нормализации.
        // Начинаем с текущего столбца col, потому что элементы левее него
        // уже не влияют на дальнейшее исключение.
        std::size_t j = col;
        for (; j < n; ++j)
        {
            // Делим строку на ведущий элемент, чтобы на диагонали получить 1.
            a[col][j] /= diag;
        }

        // Правая часть b должна делиться на тот же коэффициент, что и строка A.
        b[col] /= diag;

        // Теперь зануляем текущий столбец во всех остальных строках.
        // Для этого снова запускаем перебор строк с самого начала матрицы.
        row = 0;
        for (; row < n; ++row)
        {
            // Ведущую строку саму из себя вычитать нельзя и не нужно.
            if (row == col)
            {
                // Текущую ведущую строку саму из себя не вычитаем.
                continue;
            }

            // factor показывает, сколько ведущей строки нужно вычесть из row.
            const double factor = a[row][col];
            // Если элемент уже почти нулевой, вычитание ведущей строки только накопит шум округления.
            if (std::fabs(factor) < eps)
            {
                // Если элемент уже почти нулевой, лишнюю арифметику не делаем.
                continue;
            }

            // При вычитании строки достаточно идти с текущего столбца col,
            // потому что элементы левее уже приведены на прошлых шагах.
            j = col;
            for (; j < n; ++j)
            {
                // Вычитаем factor * ведущая_строка из текущей строки матрицы.
                a[row][j] -= factor * a[col][j];
            }

            // То же преобразование обязано применяться и к правой части системы.
            b[row] -= factor * b[col];
        }
    }

    // После приведения системы к единичной матрице справа остается готовое
    // решение.
    // Поэтому вектор b уже сам является искомым решением x.
    x = b;

    // true означает, что исключение Гаусса завершилось успешно и в x записано
    // корректное решение системы.
    return true;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BUILDQUADRATICVERIFICATIONPOINTS
// ----------------------------------------------------------------------------
// Назначение:
// Построить набор тестовых точек для проверки, действительно ли введенная
// функция квадратична.
// ----------------------------------------------------------------------------
void BuildQuadraticVerificationPoints(std::size_t dimension, std::vector<Vector>& points)
{
    // Сбрасываем старый набор контрольных точек перед построением нового.
    points.clear();

    // Первая точка — вектор из единиц.
    // Конструктор Vector(dimension, 1.0) сразу создает точку длины dimension,
    // все координаты которой равны 1.0.
    Vector p1(dimension, 1.0);

    // Добавляем первую контрольную точку в массив.
    points.push_back(p1);

    // Вторая и третья точки строятся несимметрично, чтобы лучше выявлять
    // возможные нелинейные члены.
    // Здесь стартуем с нулевого вектора и затем вручную задаем отдельные
    // координаты.
    Vector p2(dimension, 0.0);
    // Первую координату второй контрольной точки задаем только если такая координата вообще существует.
    if (dimension >= 1)
    {
        // Если пространство хотя бы одномерное, задаем первую координату.
        p2[0] = 2.0;
    }
    // Вторую координату заполняем только для задач размерности хотя бы 2.
    if (dimension >= 2)
    {
        // Во второй координате специально берем отрицательное число, чтобы
        // тестовая точка была менее симметричной.
        p2[1] = -1.0;
    }
    // Третью координату добавляем только в трехмерном случае.
    if (dimension >= 3)
    {
        // Для третьего измерения берем дробное значение, чтобы проверка
        // не ограничивалась только целыми координатами.
        p2[2] = 0.5;
    }

    // Сохраняем вторую контрольную точку.
    points.push_back(p2);

    // Третью контрольную точку тоже начинаем с нулевого вектора.
    Vector p3(dimension, 0.0);
    // Первую координату третьей контрольной точки задаем только при наличии оси x1.
    if (dimension >= 1)
    {
        // Первая координата выбирается отрицательной и отличной от значений p1/p2.
        p3[0] = -1.5;
    }
    // Вторую координату задаем только для размерности не меньше двух.
    if (dimension >= 2)
    {
        // Вторая координата делается положительной, но нецелой.
        p3[1] = 0.75;
    }
    // Третью координату определяем только когда реально есть ось x3.
    if (dimension >= 3)
    {
        // Третья координата снова выбирается отличной по знаку и масштабу.
        p3[2] = -2.0;
    }

    // Сохраняем третью контрольную точку.
    points.push_back(p3);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BUILDSTATIONARYPOINTBYDERIVATIVE
// ----------------------------------------------------------------------------
// Назначение:
// Построить стационарную точку аналитически через систему ∇f = 0 для
// квадратичной функции размерности 1..3.
// ----------------------------------------------------------------------------
void BuildStationaryPointByDerivative(const IObjectiveFunction* function,
                                      const OptimizationResult& result,
                                      StationaryPointReport& report)
                                      {
    // Сначала полностью сбрасываем отчет, чтобы не тянуть данные от старого
    // расчета.
    report = StationaryPointReport();

    // Если функции нет, аналитический расчет стационарной точки выполнить нельзя.
    if (function == NULL)
    {
        // Без целевой функции невозможно ни восстановить коэффициенты, ни
        // построить систему производных.
        report.message = "Функция не задана.";
        return;
    }

    // Размерность берем у самой функции, а не из результата метода, потому что
    // аналитическая часть должна опираться именно на математическую модель.
    const std::size_t n = function->Dimension();
    // Аналитическое восстановление коэффициентов в этой реализации ограничено размерностями от 1 до 3.
    if (n == 0 || n > 3)
    {
        // В этом приложении аналитическое восстановление реализовано только
        // для квадратичных функций малой размерности.
        report.message = "Расчет через производную поддерживается только для размерности 1..3.";
        return;
    }

    // x0 = 0 — это базовая точка, относительно которой удобно восстанавливать
    // свободный коэффициент и остальные параметры квадратичной формы.
    Vector x0(n, 0.0);

    // Здесь будет лежать f(0).
    double f0 = 0.0;

    // Буфер для человекочитаемого текста ошибки, если какое-либо вычисление
    // функции завершится неудачно.
    std::string error;

    // В качестве базовой точки восстановления коэффициентов берем ноль.
    // Без значения функции в нуле невозможно восстановить свободный коэффициент квадратичной модели.
    if (!EvaluateFunctionValue(function, x0, f0, error))
    {
        report.message = "Не удалось вычислить f(0): " + error;
        return;
    }

    // linear[i] — коэффициент при x_i.
    Vector linear(n, 0.0);

    // square[i] — коэффициент при x_i^2.
    Vector square(n, 0.0);

    // cross[i][j] — коэффициент смешанного члена x_i * x_j.
    // Внешний std::vector хранит строки матрицы, а внутренний — сами
    // коэффициенты в пределах одной строки.
    std::vector<std::vector<double> > cross(n, std::vector<double>(n, 0.0));

    // Внешний индекс объявляем заранее в учебном стиле.
    std::size_t i = 0;
    for (i = 0; i < n; ++i)
    {
        // xp соответствует точке +e_i.
        Vector xp(n, 0.0);

        // xm соответствует точке -e_i.
        Vector xm(n, 0.0);

        // В точке +e_i только i-я координата равна +1.
        xp[i] = 1.0;

        // В точке -e_i только i-я координата равна -1.
        xm[i] = -1.0;

        // fp = f(+e_i), fm = f(-e_i).
        double fp = 0.0;
        double fm = 0.0;
        // Проверяем, удалось ли вычислить значение функции в точке +e_i.
        if (!EvaluateFunctionValue(function, xp, fp, error))
        {
            // При любой ошибке вычисления аналитический отчет прекращается
            // немедленно, потому что коэффициенты уже нельзя восстановить
            // достоверно.
            report.message = "Не удалось вычислить f(ei): " + error;
            return;
        }
        // Аналогично отдельно проверяем вычисление в точке -e_i.
        if (!EvaluateFunctionValue(function, xm, fm, error))
        {
            report.message = "Не удалось вычислить f(-ei): " + error;
            return;
        }

        // Для квадратичной функции коэффициенты линейного и квадратного членов
        // можно восстановить по значениям в точках +e_i и -e_i.
        // Формула для linear[i] восстанавливает коэффициент при x_i.
        linear[i] = 0.5 * (fp - fm);

        // Формула для square[i] восстанавливает коэффициент при x_i^2 после
        // вычитания свободного члена f0.
        square[i] = 0.5 * (fp + fm) - f0;
    }

    for (i = 0; i < n; ++i)
    {
        // Для смешанных коэффициентов рассматриваем только пары i < j,
        // чтобы не дублировать симметричные элементы дважды.
        // Индекс j стартует со следующей координаты после i.
        std::size_t j = i + 1;
        for (; j < n; ++j)
        {
            // xij соответствует точке e_i + e_j.
            Vector xij(n, 0.0);

            // В этой контрольной точке i-я координата равна 1.
            xij[i] = 1.0;

            // И j-я координата тоже равна 1.
            xij[j] = 1.0;

            // fij = f(e_i + e_j).
            double fij = 0.0;
            // Без значения в точке e_i + e_j нельзя восстановить смешанный коэффициент x_i * x_j.
            if (!EvaluateFunctionValue(function, xij, fij, error))
            {
                report.message = "Не удалось вычислить f(ei+ej): " + error;
                return;
            }

            // Так восстанавливается коэффициент смешанного члена x_i * x_j.
            const double coeff = fij - f0 - linear[i] - linear[j] - square[i] - square[j];

            // Из-за симметрии квадратичной формы коэффициент записываем сразу
            // в обе зеркальные позиции матрицы.
            cross[i][j] = coeff;
            cross[j][i] = coeff;
        }
    }

    // checkPoints — набор внешних тестовых точек, на которых проверяем, что
    // восстановленная квадратичная модель действительно совпадает с функцией.
    std::vector<Vector> checkPoints;

    // Наполняем массив заранее выбранными контрольными точками.
    BuildQuadraticVerificationPoints(n, checkPoints);

    // Базовый численный допуск на сравнение реальной функции и модели.
    const double verifyTolBase = 1e-7;
    for (i = 0; i < checkPoints.size(); ++i)
    {
        // Реальное значение исходной функции в контрольной точке.
        double realValue = 0.0;
        // На каждой контрольной точке сравниваем исходную функцию с восстановленной квадратичной моделью.
        if (!EvaluateFunctionValue(function, checkPoints[i], realValue, error))
        {
            report.message = "Не удалось проверить квадратичность функции: " + error;
            return;
        }

        // Значение восстановленной квадратичной модели в той же точке.
        const double modelValue = EvaluateQuadraticModel(f0, linear, square, cross, checkPoints[i]);

        // Допуск масштабируется от величины функции, чтобы сравнение было
        // устойчивым и для маленьких, и для больших значений.
        const double tol = verifyTolBase * (1.0 + std::fabs(realValue));
        // Если расхождение между моделью и функцией слишком велико, считаем функцию неквадратичной.
        if (std::fabs(realValue - modelValue) > tol)
        {
            // Если расхождение превышает допуск, считаем, что пользователь
            // задал не квадратичную функцию, а более общую нелинейную модель.
            report.message =
                "Функция не является квадратичной (или задана с нелинейными членами), аналитическая "
                "стационарная точка по ∇f=0 недоступна.";
            return;
        }
    }

    // gradMatrix — матрица коэффициентов системы ∇f = 0.
    // Каждый ее элемент соответствует коэффициенту при очередной переменной
    // в уравнениях градиента.
    std::vector<std::vector<double> > gradMatrix(n, std::vector<double>(n, 0.0));

    // rhs — правая часть той же системы.
    Vector rhs(n, 0.0);

    for (i = 0; i < n; ++i)
    {
        // Производная по x_i дает коэффициент 2 * square[i] при x_i.
        gradMatrix[i][i] = 2.0 * square[i];

        // Линейные члены переносятся в правую часть со знаком минус.
        rhs[i] = -linear[i];
    }
    for (i = 0; i < n; ++i)
    {
        // Для пары i < j снова идем только по верхнему треугольнику матрицы.
        std::size_t j = i + 1;
        for (; j < n; ++j)
        {
            // Смешанные коэффициенты входят в матрицу производных симметрично.
            gradMatrix[i][j] = cross[i][j];
            gradMatrix[j][i] = cross[i][j];
        }
    }

    // Здесь появится решение системы ∇f = 0, то есть стационарная точка.
    Vector stationaryX;
    // Если систему ∇f = 0 решить не удалось, стационарная точка не определена однозначно.
    if (!SolveLinearSystem(gradMatrix, rhs, stationaryX))
    {
        // Если систему решить нельзя, значит аналитическая стационарная точка
        // не выражается как единственное решение линейной системы.
        report.message = "Система ∇f=0 вырождена: стационарная точка не единственна или отсутствует.";
        return;
    }

    // Значение функции в найденной аналитической стационарной точке.
    double stationaryValue = 0.0;
    // После нахождения аналитической точки обязательно считаем значение функции в ней.
    if (!EvaluateFunctionValue(function, stationaryX, stationaryValue, error))
    {
        report.message = "Не удалось вычислить f(x_ст): " + error;
        return;
    }

    // Если дошли сюда, аналитическая часть полностью построена успешно.
    // available = true — главный флаг, по которому GUI понимает, что блок со
    // стационарной точкой можно показывать как валидный результат.
    report.available = true;

    // Сохраняем найденные координаты стационарной точки.
    report.point = stationaryX;

    // Сохраняем значение функции в этой точке.
    report.value = stationaryValue;

    // Сравнивать точки можно только если численный и аналитический векторы имеют одинаковую размерность.
    if (result.optimumX.size() == stationaryX.size())
    {
        // sum накапливает квадрат евклидовой дистанции между аналитической и
        // численной точками.
        double sum = 0.0;
        for (i = 0; i < stationaryX.size(); ++i)
        {
            // d — разность координат двух решений по текущему измерению.
            const double d = stationaryX[i] - result.optimumX[i];
            sum += d * d;
        }

        // Сравниваем аналитическую точку со значением, полученным методом.
        report.distanceToMethodPoint = std::sqrt(sum);
        report.deltaValueToMethod = std::fabs(stationaryValue - result.optimumValue);
    }
}

