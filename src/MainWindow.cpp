// ----------------------------------------------------------------------------
// ФУНКЦИЯ SANITIZEFILENAMEPART
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать произвольный текст в безопасный фрагмент имени файла.
// ----------------------------------------------------------------------------
QString SanitizeFileNamePart(const QString& text, int maxLength)
{
    // Сначала убираем внешние пробелы.
    // trimmed() возвращает копию строки без пробелов и переводов строки по краям.
    QString result = text.trimmed();

    // Приводим возможную десятичную запятую к точке для единообразия.
    result.replace(',', '.');

    // Все символы, которые могут быть неудобны в имени файла, заменяем
    // подчеркиванием.
    result.replace(QRegularExpression(QString::fromUtf8("[^A-Za-z0-9_\\-\\.]+")), QString::fromUtf8("_"));

    // Сжимаем подряд идущие подчеркивания в одно.
    result.replace(QRegularExpression(QString::fromUtf8("_+")), QString::fromUtf8("_"));

    // Убираем лишние служебные символы по краям строки.
    result.replace(QRegularExpression(QString::fromUtf8("^[_\\.\\-]+|[_\\.\\-]+$")), QString::fromUtf8(""));

    // Если после очистки строка полностью исчезла, подставляем безопасный текст-заполнитель.
    if (result.isEmpty())
    {
        // Если после очистки от исходного текста ничего не осталось,
        // подставляем безопасное резервное имя.
        result = QString::fromUtf8("value");
    }

    // Если строка слишком длинная, обрезаем ее и снова чистим края.
    // Слишком длинный фрагмент имени файла принудительно обрезаем до допустимой длины.
    if (result.size() > maxLength)
    {
        // left(maxLength) оставляет только первые maxLength символов строки.
        result = result.left(maxLength);

        // После обрезки снова удаляем возможные служебные символы на краях,
        // потому что граница разреза могла пройти по '_' , '.' или '-'.
        result.replace(QRegularExpression(QString::fromUtf8("^[_\\.\\-]+|[_\\.\\-]+$")), QString::fromUtf8(""));
    }

    // После обрезки строка снова могла стать пустой, поэтому повторяем
    // защитную подстановку.
    // После обрезки и очистки строка снова может стать пустой, поэтому повторно страхуемся.
    if (result.isEmpty())
    {
        result = QString::fromUtf8("value");
    }

    // Возвращаем уже очищенный и безопасный фрагмент имени файла.
    return result;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ VECTORTOFILENAMEPART
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать числовой вектор в компактный и безопасный фрагмент имени файла.
// ----------------------------------------------------------------------------
QString VectorToFileNamePart(const Vector& values)
{
    // QStringList удобен для накопления нескольких строковых частей с
    // последующим объединением через join(...).
    QStringList parts;

    // Индекс цикла объявляем заранее в простом учебном стиле.
    std::size_t i = 0;
    for (i = 0; i < values.size(); ++i)
    {
        // Формат 'g' дает короткую запись числа без лишних нулей.
        // Локальная строка number нужна, потому что далее ее еще нужно
        // дополнительно очистить под имя файла.
        QString number = QString::number(values[i], 'g', 8);
        number.replace(',', '.');

        // Минус заменяем на 'm', а точку — на '_', чтобы имя файла состояло
        // из безопасных символов.
        number.replace('-', 'm');
        number.replace('.', '_');

        // Оператор << для QStringList дописывает новый элемент в конец списка.
        parts << SanitizeFileNamePart(number, 24);
    }

    // Если вектор пуст, возвращаем специальный текстовый маркер вместо пустой строки.
    if (parts.isEmpty())
    {
        // Пустой вектор кодируем специальной строкой "none", чтобы имя файла
        // все равно оставалось валидным и информативным.
        return QString::fromUtf8("none");
    }

    // Все координаты соединяются символом "_".
    // join(...) формирует одну строку из всех частей списка.
    return parts.join(QString::fromUtf8("_"));
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BUILDTRACEEXPORTFILENAME
// ----------------------------------------------------------------------------
// Назначение:
// Собрать имя PNG-файла трассировки из параметров текущего расчета.
// ----------------------------------------------------------------------------
QString BuildTraceExportFileName(const QString& expression, const OptimizationRequest& request)
{
    // Основные части имени строятся из выражения, начальной точки и численных
    // параметров метода.
    // Каждый фрагмент очищается отдельно, чтобы итоговая строка оставалась
    // читаемой и при этом безопасной для файловой системы.
    const QString exprPart = SanitizeFileNamePart(expression, 48);
    const QString x0Part = VectorToFileNamePart(request.initialPoint);
    const QString epsilonPart = SanitizeFileNamePart(QString::number(request.options.epsilon, 'g', 8), 16);
    const QString alphaPart = SanitizeFileNamePart(QString::number(request.options.alpha, 'g', 8), 16);
    const QString betaPart = SanitizeFileNamePart(QString::number(request.options.beta, 'g', 8), 16);

    // Если стартовые шаги не заданы явно, в имени файла оставляем метку "none".
    QString stepPart = QString::fromUtf8("none");
    // Если пользователь явно задал стартовые длины шагов, берем первый из них для имени файла.
    if (!request.options.initialStepMagnitudes.empty())
    {
        // Сейчас в имени файла используется первый базовый шаг. Этого
        // достаточно, потому что в большинстве запусков все стартовые шаги
        // одинаковы.
        stepPart =
            SanitizeFileNamePart(QString::number(request.options.initialStepMagnitudes[0], 'g', 8), 16);
    }

    // QString::arg(...) последовательно подставляет подготовленные части в
    // шаблон будущего имени файла.
    QString fileName = QString::fromUtf8("trace_%1__x0_%2__eps_%3__a_%4__b_%5__d0_%6")
                           .arg(exprPart)
                           .arg(x0Part)
                           .arg(epsilonPart)
                           .arg(alphaPart)
                           .arg(betaPart)
                           .arg(stepPart);

    // Финальная очистка защищает от случайных длинных или неудобных имен.
    fileName = SanitizeFileNamePart(fileName, 180);

    // Возвращаем полное имя PNG-файла уже с расширением.
    return fileName + QString::fromUtf8(".png");
}

