// ----------------------------------------------------------------------------
// СТРУКТУРА STATIONARYPOINTREPORT
// ----------------------------------------------------------------------------
// Назначение:
// Временно хранить информацию о стационарной точке, найденной через решение
// системы ∇f = 0 для квадратичной функции.
//
// Зачем нужна отдельная структура:
// 1) метод Розенброка дает численное решение;
// 2) для квадратичных функций полезно отдельно посчитать аналитическую
//    стационарную точку;
// 3) затем эти два результата можно сравнить в GUI.
// ----------------------------------------------------------------------------
struct StationaryPointReport
{
    // Флаг того, удалось ли вообще построить аналитический отчет.
    bool available;

    // Координаты стационарной точки x_ст.
    Vector point;

    // Значение функции в стационарной точке f(x_ст).
    double value;

    // Евклидово расстояние между x_ст и численным ответом метода.
    double distanceToMethodPoint;

    // Разница значений функции между аналитическим и численным ответом.
    double deltaValueToMethod;

    // Текст пояснения, если аналитическая точка недоступна или если надо
    // показать дополнительный комментарий пользователю.
    std::string message;

    StationaryPointReport()
        : available(false),
          value(0.0),
          distanceToMethodPoint(0.0),
          deltaValueToMethod(0.0)
          {
        // Структура создается в нейтральном состоянии:
        // аналитический отчет пока недоступен, а все численные поля равны
        // нулю.
    }
};

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FORMATDOUBLE
// ----------------------------------------------------------------------------
// Назначение:
// Перевести вещественное число в строку Qt с заданной точностью.
// ----------------------------------------------------------------------------
QString FormatDouble(double value, int precision)
{
    // Если число не является конечным, явно выводим "nan", а не пытаемся
    // форматировать его обычным способом.
    // Если число не конечно, такое значение нельзя безопасно показывать и использовать в расчетах.
    if (!std::isfinite(value))
    {
        return QString::fromUtf8("nan");
    }

    // QString::number(...) строит строку числа.
    // Символ 'f' означает фиксированный формат записи с precision знаками
    // после запятой.
    return QString::number(value, 'f', precision);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FORMATVECTOR
// ----------------------------------------------------------------------------
// Назначение:
// Перевести числовой вектор в строку вида "(a, b, c)" для GUI.
// ----------------------------------------------------------------------------
QString FormatVector(const Vector& values, int precision)
{
    // Начинаем собирать строку с открывающей круглой скобки.
    QString text = "(";

    // Индекс объявляется заранее отдельной строкой в простом учебном стиле,
    // чтобы было явно видно, чем управляется дальнейший цикл.
    std::size_t i = 0;
    for (i = 0; i < values.size(); ++i)
    {
        // Разделитель между элементами добавляется только начиная со второго
        // числа.
        // Разделитель нужен только между уже записанными элементами вектора.
        if (i > 0)
        {
            text += QString::fromUtf8(", ");
        }

        // Для каждого элемента используем тот же helper FormatDouble(...),
        // чтобы форматирование было единообразным по всему приложению.
        text += FormatDouble(values[i], precision);
    }

    // После завершения цикла закрываем строковое представление вектора
    // правой круглой скобкой.
    text += ")";

    // Возвращаем уже полностью готовую строку вида "(a, b, c)".
    return text;
}

