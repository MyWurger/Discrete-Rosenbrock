// ============================================================================
// КЛАСС FunctionF1 - РЕАЛИЗАЦИЯ ДВУМЕРНОЙ ФУНКЦИИ F1
// ============================================================================
// Реализует функцию:
// F1(x1, x2) = 9*x1^2 + 16*x2^2 - 90*x1 - 128*x2
// ============================================================================
class FunctionF1 : public IObjectiveFunction
{
public:
    // Возвращает размерность пространства переменных для F1.
    std::size_t Dimension() const
    {
        return 2;
    }

    // ------------------------------------------------------------------------
    // МЕТОД Evaluate - ВЫЧИСЛЕНИЕ F1 В ТОЧКЕ x = (x1, x2)
    // ------------------------------------------------------------------------
    // Принимает:
    // - x: вектор координат точки.
    // Записывает:
    // - value: значение функции при успешном вычислении;
    // - error: текст ошибки, если вычисление выполнить нельзя.
    // Возвращает:
    // - true, если значение функции успешно вычислено;
    // - false, если размерность неверна или результат некорректен.
    // ------------------------------------------------------------------------
    bool Evaluate(const Vector& x, double& value, std::string& error) const
    {
        // Проверяем, что передана именно двумерная точка.
        if (x.size() != 2)
        {
            error = "Размерность точки не совпадает с размерностью функции";
            return false;
        }

        // Выделяем координаты в отдельные локальные переменные для читаемости формулы.
        const double x1 = x[0];
        const double x2 = x[1];

        // Вычисляем значение квадратичной функции F1 по формуле из задания.
        value = 9.0 * x1 * x1 + 16.0 * x2 * x2 - 90.0 * x1 - 128.0 * x2;

        // Проверяем, что результат не ушел в NaN или бесконечность.
        if (!std::isfinite(value))
        {
            error = "Вычисление функции дало неконечное значение";
            return false;
        }

        // Вычисление завершилось успешно.
        return true;
    }

    // Возвращает полное имя функции для GUI и таблиц.
    const std::string& Name() const
    {
        return name_;
    }

private:
    // Храним готовое текстовое имя функции в виде "F1(x1, x2) = ...".
    std::string name_ = std::string("F1(x1, x2) = ") + FUNCTION_F1_EXPRESSION_TEXT;
};

// ============================================================================
// КЛАСС FunctionF2 - РЕАЛИЗАЦИЯ ТРЕХМЕРНОЙ ФУНКЦИИ F2
// ============================================================================
// Реализует функцию:
// F2(x1, x2, x3) = x1^2 + 2*x1*x2 + 2*x2^2 + x3^2 - x2*x3 + x1 + 3*x2 - x3
// ============================================================================
class FunctionF2 : public IObjectiveFunction
{
public:
    // Возвращает размерность пространства переменных для F2.
    std::size_t Dimension() const
    {
        return 3;
    }

    // ------------------------------------------------------------------------
    // МЕТОД Evaluate - ВЫЧИСЛЕНИЕ F2 В ТОЧКЕ x = (x1, x2, x3)
    // ------------------------------------------------------------------------
    // Принимает:
    // - x: вектор координат точки.
    // Записывает:
    // - value: значение функции при успешном вычислении;
    // - error: текст ошибки, если вычисление выполнить нельзя.
    // Возвращает:
    // - true, если значение функции успешно вычислено;
    // - false, если размерность неверна или результат некорректен.
    // ------------------------------------------------------------------------
    bool Evaluate(const Vector& x, double& value, std::string& error) const
    {
        // Проверяем, что передана именно трехмерная точка.
        if (x.size() != 3)
        {
            error = "Размерность точки не совпадает с размерностью функции";
            return false;
        }

        // Выделяем координаты в отдельные локальные переменные для читаемости выражения.
        const double x1 = x[0];
        const double x2 = x[1];
        const double x3 = x[2];

        // Вычисляем значение квадратичной функции F2 по формуле из задания.
        value = x1 * x1 + 2.0 * x1 * x2 + 2.0 * x2 * x2 + x3 * x3 - x2 * x3 + x1 + 3.0 * x2 -
                x3;

        // Проверяем, что значение функции осталось конечным.
        if (!std::isfinite(value))
        {
            error = "Вычисление функции дало неконечное значение";
            return false;
        }

        // Вычисление завершилось успешно.
        return true;
    }

    // Возвращает полное имя функции для GUI и таблиц.
    const std::string& Name() const
    {
        return name_;
    }

private:
    // Храним готовое текстовое имя функции в виде "F2(x1, x2, x3) = ...".
    std::string name_ = std::string("F2(x1, x2, x3) = ") + FUNCTION_F2_EXPRESSION_TEXT;
};

