// ----------------------------------------------------------------------------
// ФУНКЦИЯ ANNOTATELASTPASSROWS
// ----------------------------------------------------------------------------
// Назначение:
// Отметить последние n строк трассировки, которые соответствуют только что
// завершившемуся полному проходу по всем направлениям.
//
// Зачем это нужно:
// Сам математический алгоритм оперирует проходами по базису направлений.
// Однако в таблице GUI каждая строка соответствует одному отдельному шагу
// по одному направлению s_j. Поэтому, когда в конце полного прохода
// фиксируется:
// - откат,
// - перестройка базиса,
// - новый базис,
// эту информацию нужно аккуратно проставить в уже добавленные строки.
//
// Принимает:
// - result            : итоговую структуру с накопленной трассировкой;
// - dimension         : размерность задачи, равная числу направлений в проходе;
// - rollback          : надо ли отметить факт отката;
// - directionChanged  : надо ли отметить факт перестройки направлений;
// - newBasis          : текстовое представление нового базиса;
// - newBasisDirections: сами новые направления для графика;
// - newBasisOrigin    : точку, из которой базис нужно рисовать.
//
// Изменяет:
// - последние dimension строк массива result.trace.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
static void AnnotateLastPassRows(OptimizationResult& result,
                                 std::size_t dimension,
                                 bool rollback,
                                 bool directionChanged,
                                 const std::string& newBasis,
                                 const std::vector<Vector>& newBasisDirections,
                                 const Vector& newBasisOrigin)
{
    // Если в трассировке пока меньше строк, чем размерность задачи, то
    // завершенного полного прохода еще просто нет. В такой ситуации ничего
    // размечать не нужно.
    if (result.trace.size() < dimension)
    {
        return;
    }

    // Последний полный проход состоит ровно из dimension строк.
    // Поэтому его первая строка находится по индексу:
    //     trace.size() - dimension.
    const std::size_t start = result.trace.size() - dimension;

    for (std::size_t i = 0; i < dimension; ++i)
    {
        // Берем ссылку именно на существующую строку в trace, чтобы менять
        // данные прямо внутри массива, а не работать с временной копией.
        OptimizationResult::TraceRow& row = result.trace[start + i];

        // Визуально откат и перестройка базиса фиксируются не у каждой строки
        // прохода, а на последней строке этого прохода. Так легче читать
        // таблицу: видно, что событие произошло именно по завершении полного
        // обхода всех направлений.
        const bool isLastRowInPass = (i + 1 == dimension);

        // rollback и directionChanged приходят сюда как признаки, относящиеся
        // ко всему завершенному проходу по базису.
        //
        // Но в таблице мы намеренно показываем эти события только на
        // последней строке прохода, чтобы:
        // 1) не дублировать один и тот же факт в каждой строке;
        // 2) визуально привязать событие к моменту завершения полного обхода
        //    всех направлений;
        // 3) сделать таблицу ближе к логике методички, где откат или
        //    перестройка базиса фиксируются после проверки всего прохода.
        row.rollback = (rollback && isLastRowInPass);
        row.directionChanged = (directionChanged && isLastRowInPass);

        if (row.directionChanged)
        {
            // Если на последней строке прохода произошла перестройка базиса,
            // сохраняем:
            // 1) строковое представление базиса;
            // 2) сами новые направления;
            // 3) точку начала нового базиса.
            row.newBasis = newBasis;
            row.newBasisDirections = newBasisDirections;
            row.newBasisOrigin = newBasisOrigin;
        }
        else
        {
            // Иначе очищаем эти поля, чтобы в таблице не "тянулись" старые
            // значения от предыдущих проходов.
            row.newBasis.clear();
            row.newBasisDirections.clear();
            row.newBasisOrigin.clear();
        }
    }
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FORMATVECTORSHORT
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать числовой вектор в компактную строку вида "(a, b, c)".
//
// Зачем это нужно:
// GUI-таблица и итоговые строки требуют короткого текстового представления
// точек, направлений и наборов шагов.
//
// Принимает:
// - values : вектор чисел для форматирования.
//
// Возвращает:
// - строку с числами, записанными с 4 знаками после запятой.
// ----------------------------------------------------------------------------
static std::string FormatVectorShort(const Vector& values)
{
    // Поток создается локально внутри функции, потому что строка собирается
    // заново при каждом вызове и не должна хранить состояние между вызовами.
    std::ostringstream out;

    // Сначала открываем круглую скобку.
    out << "(";

    for (std::size_t i = 0; i < values.size(); ++i)
    {
        // Начиная со второго элемента, добавляем разделитель.
        if (i > 0)
        {
            out << ", ";
        }

        // std::fixed фиксирует формат вещественных чисел, а setprecision(4)
        // задает 4 знака после запятой.
        out << std::fixed << std::setprecision(4) << values[i];
    }

    // Закрываем круглую скобку и возвращаем собранную строку.
    out << ")";
    return out.str();
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FORMATBASIS
// ----------------------------------------------------------------------------
// Назначение:
// Сформировать строку с описанием базиса направлений в виде
// "[s1=(...), s2=(...), ...]".
//
// Принимает:
// - directions : набор направлений текущего или нового базиса.
//
// Возвращает:
// - строковое представление всего базиса.
// ----------------------------------------------------------------------------
static std::string FormatBasis(const std::vector<Vector>& directions)
{
    // ostringstream — это поток для записи в строку.
    // Он удобен тем, что позволяет собирать итоговую строку по частям почти
    // так же, как если бы мы печатали данные в cout.
    std::ostringstream out;

    // Открываем квадратную скобку, чтобы получить формат:
    //     [s1=(...), s2=(...), ...]
    out << "[";

    for (std::size_t i = 0; i < directions.size(); ++i)
    {
        // Разделитель "; " нужен между направлениями, но не перед первым
        // элементом, поэтому проверка стоит именно на i > 0.
        if (i > 0)
        {
            out << "; ";
        }

        // Здесь последовательно записываем:
        // 1) имя направления s1, s2, ...;
        // 2) знак "=";
        // 3) компактное строковое представление самого вектора.
        out << "s" << (i + 1) << "=" << FormatVectorShort(directions[i]);
    }

    // Закрываем квадратную скобку и возвращаем готовую строку.
    out << "]";
    return out.str();
}

