// ============================================================================
// ФУНКЦИЯ ToStdString - ПЕРЕВОД СТРОКИ MUPARSER В std::string
// ============================================================================
// muparser использует собственный строковый тип mu::string_type.
// В остальном проекте мы работаем с обычным std::string, поэтому нужен
// аккуратный переход между этими типами.
// Принимает:
// - text: строка muparser.
// Возвращает:
// - обычный std::string с тем же содержимым.
// ============================================================================
static std::string ToStdString(const mu::string_type& text)
{
    // Конструируем std::string по диапазону символов mu::string_type.
    // Такой способ не зависит от внутренней реализации mu::string_type и
    // работает как для narrow-, так и для compatible wide-типов строки.
    return std::string(text.begin(), text.end());
}

// ============================================================================
// ФУНКЦИЯ BuildMuParserError - ПОСТРОЕНИЕ ПОНЯТНОГО ТЕКСТА ОШИБКИ MUPARSER
// ============================================================================
// Собирает единое человеко-читаемое сообщение из исключения muparser:
// 1) основной текст ошибки;
// 2) позиция в строке, если parser смог ее определить;
// 3) проблемный токен, если он доступен.
// Принимает:
// - parserError: исключение, полученное от muparser.
// Возвращает:
// - готовую строку ошибки, пригодную для GUI и логики валидации.
// ============================================================================
static std::string BuildMuParserError(const mu::Parser::exception_type& parserError)
{
    // Берем базовое текстовое сообщение из muparser и приводим его к std::string.
    std::string message = ToStdString(parserError.GetMsg());

    // Получаем позицию символа, на котором parser обнаружил проблему.
    // Значение >= 0 означает, что позиция действительно известна.
    const int position = parserError.GetPos();
    if (position >= 0)
    {
        // Дописываем позицию к сообщению, чтобы пользователь видел место ошибки в формуле.
        message += " (позиция: " + std::to_string(position) + ")";
    }

    // Получаем токен (фрагмент текста), вызвавший ошибку, если muparser его сохранил.
    const std::string token = ToStdString(parserError.GetToken());
    if (!token.empty())
    {
        // Если токен известен, добавляем и его тоже.
        message += ", токен: " + token;
    }

    // Возвращаем полностью собранный текст.
    return message;
}

// ============================================================================
// КЛАСС ParsedMuParserFunction - КОНКРЕТНАЯ МНОГОМЕРНАЯ ФУНКЦИЯ НА БАЗЕ MUPARSER
// ============================================================================
// Этот класс:
// - хранит имя и текст выражения;
// - создает и настраивает mu::Parser;
// - привязывает x1, x2, x3 к элементам массива variables_;
// - реализует интерфейс IObjectiveFunction.
// ============================================================================
class ParsedMuParserFunction : public IObjectiveFunction
{
public:
    // Конструктор по умолчанию.
    // Сразу выставляем dimension_ в 0, чтобы можно было распознавать
    // неинициализированное состояние объекта.
    ParsedMuParserFunction() : dimension_(0)
    {
    }

    // ------------------------------------------------------------------------
    // МЕТОД Initialize - ПОДГОТОВКА ПАРСЕРА И ПРОВЕРКА ФОРМУЛЫ
    // ------------------------------------------------------------------------
    // Шаги:
    // 1) валидируем имя, выражение и размерность;
    // 2) сохраняем имя/текст/размерность внутрь объекта;
    // 3) создаем массив переменных нужной длины;
    // 4) регистрируем x1, x2, ..., xn и короткое имя x;
    // 5) регистрируем константы pi и e;
    // 6) передаем выражение parser-у;
    // 7) делаем пробный Eval(), чтобы поймать синтаксические ошибки заранее.
    // Принимает:
    // - name: имя функции для таблиц и GUI;
    // - expression: текст математической формулы;
    // - dimension: размерность пространства переменных;
    // Записывает:
    // - error: текст ошибки, если инициализация завершится неудачно.
    // Возвращает:
    // - true, если функция успешно инициализирована;
    // - false, если входные данные некорректны или parser не смог принять формулу.
    // ------------------------------------------------------------------------
    bool Initialize(const std::string& name,
                    const std::string& expression,
                    std::size_t dimension,
                    std::string& error)
    {
        // Сначала очищаем предыдущий текст ошибки, чтобы не держать устаревшее сообщение.
        error.clear();

        // Пустое имя не допускаем: оно используется в таблицах, графиках и итогах GUI.
        if (name.empty())
        {
            error = "Имя функции не должно быть пустым";
            return false;
        }

        // Пустую формулу передавать в parser бессмысленно.
        if (expression.empty())
        {
            error = "Выражение не должно быть пустым";
            return false;
        }

        // Размерность задачи должна быть положительной.
        if (dimension == 0)
        {
            error = "Размерность должна быть положительной";
            return false;
        }

        // Сохраняем имя функции внутрь объекта для дальнейшего возврата через Name().
        name_ = name;
        // Сохраняем оригинальный текст выражения.
        expression_ = expression;
        // Сохраняем размерность.
        dimension_ = dimension;
        // Подготавливаем массив переменных нужной длины и инициализируем его нулями.
        // assign(...) удобен тем, что сразу задает и размер, и начальное значение элементов.
        variables_.assign(dimension_, 0.0);

        try
        {
            // Регистрируем имена x1, x2, ..., xn.
            // Каждый DefineVar связывает строковое имя переменной в формуле
            // с конкретным адресом double внутри variables_.
            std::size_t i = 0;
            for (i = 0; i < dimension_; ++i)
            {
                parser_.DefineVar("x" + std::to_string(i + 1), &variables_[i]);
            }

            // Дополнительно регистрируем короткое имя x как alias для x1.
            // Это полезно для одномерных выражений и делает parser совместимым
            // со старым форматом вида "x^2 + 1".
            parser_.DefineVar("x", &variables_[0]);

            // Регистрируем математическую константу pi.
            parser_.DefineConst("pi", MATH_PI_VALUE);
            // Регистрируем математическую константу e.
            parser_.DefineConst("e", MATH_E_VALUE);

            // Передаем полный текст выражения в muparser.
            // Parser не вычисляет выражение окончательно на этом шаге, а только
            // подготавливает внутреннее представление.
            parser_.SetExpr(expression_);

            // Делаем пробное вычисление сразу.
            // (void) нужен только чтобы явно показать: возвращаемое значение здесь
            // нам не важно, важен сам факт успешного Eval() без исключения.
            (void)parser_.Eval();
        }
        catch (const mu::Parser::exception_type& ex)
        {
            // Если muparser выдал исключение, конвертируем его в понятное сообщение.
            error = "Ошибка разбора выражения: " + BuildMuParserError(ex);
            return false;
        }

        // Если все шаги прошли успешно, объект готов к вычислениям.
        return true;
    }

    // Возвращает размерность функции.
    std::size_t Dimension() const
    {
        return dimension_;
    }

    // ------------------------------------------------------------------------
    // МЕТОД Evaluate - ВЫЧИСЛЕНИЕ ФУНКЦИИ В МНОГОМЕРНОЙ ТОЧКЕ
    // ------------------------------------------------------------------------
    // Принимает:
    // - x: вектор координат точки, в которой нужно вычислить функцию.
    // Записывает:
    // - value: значение функции при успешном вычислении;
    // - error: текст ошибки, если вычисление завершится неудачно.
    // Возвращает:
    // - true, если значение успешно вычислено;
    // - false, если объект не инициализирован, размерность точки неверна,
    //   координаты некорректны или muparser выдал ошибку.
    // ------------------------------------------------------------------------
    bool Evaluate(const Vector& x, double& value, std::string& error) const
    {
        // Сбрасываем старый текст ошибки перед новым вычислением.
        error.clear();

        // Если размерность равна нулю, Initialize(...) не вызывался или завершился неуспешно.
        if (dimension_ == 0)
        {
            error = "Функция не инициализирована";
            return false;
        }

        // Размерность точки обязана совпадать с размерностью функции.
        if (x.size() != dimension_)
        {
            error = "Размерность точки не совпадает с размерностью функции";
            return false;
        }

        // Переписываем все координаты входной точки в массив variables_,
        // к элементам которого уже привязан parser.
        std::size_t i = 0;
        for (i = 0; i < dimension_; ++i)
        {
            // Каждая координата обязана быть конечным числом.
            // std::isfinite защищает от NaN, +Inf и -Inf, которые ломают численные методы.
            if (!std::isfinite(x[i]))
            {
                error = "Точка должна содержать только конечные значения";
                return false;
            }

            // variables_ объявлен как mutable, потому что формально метод const,
            // но для вычисления нужно временно подставить новые значения переменных.
            variables_[i] = x[i];
        }

        try
        {
            // После обновления variables_ parser использует уже новые значения x1..xn.
            value = parser_.Eval();
        }
        catch (const mu::Parser::exception_type& ex)
        {
            // Любую ошибку вычисления переводим в человеко-читаемое сообщение.
            error = "Ошибка вычисления выражения: " + BuildMuParserError(ex);
            return false;
        }

        // Дополнительно проверяем, что итог вычисления конечный.
        // Это отлавливает случаи вроде деления на ноль, переполнения или недопустимых операций.
        if (!std::isfinite(value))
        {
            error = "Вычисление выражения дало неконечное значение";
            return false;
        }

        // Значение успешно вычислено.
        return true;
    }

    // Возвращает сохраненное имя функции.
    const std::string& Name() const
    {
        return name_;
    }

private:
    std::string name_;                 // Имя функции для GUI, таблиц и отчетов.
    std::string expression_;           // Оригинальный текст выражения.
    std::size_t dimension_;            // Размерность функции.

    mutable std::vector<double> variables_;   // Массив значений x1..xn, к которым привязан parser.
    mutable mu::Parser parser_;               // Экземпляр muparser для компиляции и вычисления выражения.
};

// ============================================================================
// МЕТОД MuParserObjectiveParser::Parse - СОЗДАНИЕ ГОТОВОЙ ЦЕЛЕВОЙ ФУНКЦИИ
// ============================================================================
// Принимает:
// - expression: текст математической формулы пользователя;
// - dimension: ожидаемая размерность пространства;
// - name: имя функции для GUI и таблиц;
// Записывает:
// - output: указатель на созданный объект функции при успехе;
// - error: текст ошибки при неудаче.
// Возвращает:
// - true, если функция успешно создана;
// - false, если выражение, размерность или инициализация некорректны.
// ============================================================================
bool MuParserObjectiveParser::Parse(const std::string& expression,
                                    std::size_t dimension,
                                    const std::string& name,
                                    IObjectiveFunction*& output,
                                    std::string& error) const
{
    // Сразу обнуляем выходной указатель.
    // Это важно, чтобы вызывающий код не получил "висячий" адрес при раннем возврате.
    output = NULL;

    // Очищаем предыдущий текст ошибки.
    error.clear();

    // Нулевая размерность для многомерной задачи запрещена.
    if (dimension == 0)
    {
        error = "Размерность должна быть положительной";
        return false;
    }

    // Дополнительно ограничиваем parser размерностью проекта.
    if (dimension > MAX_PARSER_DIMENSION)
    {
        error = "Парсер поддерживает размерность не больше 3";
        return false;
    }

    // Создаем конкретный объект функции динамически.
    // new здесь используется осознанно, потому что контракт Parse(...) возвращает
    // наружу именно "сырой" указатель IObjectiveFunction*.
    ParsedMuParserFunction* function = new ParsedMuParserFunction();

    // Если внутренняя инициализация не удалась, освобождаем объект вручную и
    // возвращаем false, чтобы не получить утечку памяти.
    if (!function->Initialize(name, expression, dimension, error))
    {
        delete function;
        return false;
    }

    // Передаем владение вызывающей стороне через выходной указатель.
    output = function;
    return true;
}

